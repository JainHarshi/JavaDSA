package Coaching.April30;

import java.util.Scanner;

public class CodeChef5 {
    	public static void main (String[] args) throws java.lang.Exception
	{
	    Scanner sc = new Scanner(System.in);
		int n = sc.nextInt();
		while(n-- >0){
		    int p = sc.nextInt();
		    int p2 = sc.nextInt();
		    int p3 = sc.nextInt();
		    System.out.println((p-p2)* p3);
		}
	}
}

